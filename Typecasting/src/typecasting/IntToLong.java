package typecasting;

public class IntToLong {
	public static void main(String[] args) {
		System.out.println("main start");
		int a = 18;
		long l = a;
		System.out.println("This is int data " + a);
		System.out.println("This is long data" + l);
		
		System.out.println("main ends");
		
		
	}
 
}
