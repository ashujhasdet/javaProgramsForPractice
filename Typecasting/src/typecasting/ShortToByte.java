package typecasting;

public class ShortToByte {
	public static void main(String[] args) {
		System.out.println("main start");
		short s = 98;
		byte b = (byte) s;
		System.out.println("This is short data "+ s);
		System.out.println("This is byte data "+ b);
		System.out.println("main ends");
		
		
	}

}
