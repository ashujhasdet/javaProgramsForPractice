package typecasting;

public class ByteToShort {
	public static void main(String[] args) {
		System.out.println("main start");
		// This ia a program for widening typecasting for converting byte data to short
		byte a = 90;
		short s = a;
		System.out.println(a + " This is byte data");
		System.out.println(s + " This is short data");
		
		
		System.out.println("main ends");
		
	}

}
