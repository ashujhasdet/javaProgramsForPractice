package typecasting;

public class DoubleToFloat {
	public static void main(String[] args) {
		System.out.println("main start");
		double d = 98.98765;
		float c = (float) d;
		System.out.println("This is double data "+ d);
		System.out.println("This is float data "+ c);
		System.out.println("main ends");
		
		
	}


}
