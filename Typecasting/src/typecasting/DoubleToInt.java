package typecasting;

public class DoubleToInt {
	public static void main(String[] args) {
		System.out.println("main start");
		double d = 98.98765;
		int c = (int) d;
		System.out.println("This is double data "+ d);
		System.out.println("This is integer data "+ c);
		System.out.println("main ends");
		
		
	}

}
