package typecasting;

public class LongToInt {
	public static void main(String[] args) {
		System.out.println("main start");
		long x = 1234;
		int y = (int) x;
		System.out.println("This is long data "+ x);
		System.out.println("This is int data "+ y);
		System.out.println("main ends");
		
		
	}


}
